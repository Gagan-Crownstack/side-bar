function Collapse() {
    const sidebar = document.getElementById('sideBar');
    const titles = document.querySelectorAll('#dash-title');
    const links= document.getElementById('links');
    const collapselinks= document.getElementById('collapse-links');
    
    if (!isCollapse) {
        sidebar.style.width = "80px";
        sidebar.style.padding="48px 4px";
        titles.forEach(title => title.style.display = "none"
            
        );
        links.style.display="none"
        collapselinks.style.display="block"
    } else {
        sidebar.style.width = "288px";
        sidebar.style.padding="48px 24px";
        titles.forEach(title => title.style.display = "flex");
        links.style.display="flex"
        collapselinks.style.display="none"
        
    }
    isCollapse = !isCollapse;
    
}

